class colors:
    PURPLE = '\033[95m'
    RED       = '\033[31;1m'
    GREEN     = '\033[32;1m'
    YELLOW    = '\033[33;1m'
    BLUE      = '\033[34;1m'
    MAGENTA   = '\033[35;1m'
    CYAN      = '\033[36;1m'
    STEEL      = '\033[36;1m'
    ENDC      = '\033[0m'

MARK_MAP = {
    0: colors.GREEN + "#",
    1: colors.ENDC + "#",
    2: colors.YELLOW + "#",
    3: colors.BLUE + "#",
    6: colors.PURPLE + "*",
    7: colors.STEEL + "*",
    8: colors.RED + "S",
    9: colors.RED + "T",
}

# cost for terrain
COST = {
    0: 0,
    2: 4,
    3: 2,
    8: 0,
    9: 0,
}

def render_maze(maze):
    for i in range(len(maze)):
        for j in range(len(maze[i])):
            print(MARK_MAP[maze[i][j]], end=' ')
        print("\n", end="")

def run_algorithm(maze, method='uni', render=False):
    for i in range(len(maze)):
        for j in range(len(maze[0])):
            if maze[i][j] == 8:
                start = (i, j)
            if maze[i][j] == 9:
                end = (i, j)
    if method == "uni":
        path = astar_search(maze, start, end, COST)
        if render:
            for point in path:
                if point != end:
                    maze[point[0]][point[1]] = 6
            render_maze(maze)
    if method == "bi":
        path = bidirectional_astar_search(maze, start, end, COST)
        if render:
            for point in path[0]:
                if point != end:
                    maze[point[0]][point[1]] = 6
            for point in path[1]:
                if point != start:
                    maze[point[0]][point[1]] = 7
            render_maze(maze)
        
        

